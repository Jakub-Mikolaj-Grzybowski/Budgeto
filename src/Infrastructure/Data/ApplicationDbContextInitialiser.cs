                    Icon = "laptop",
                    Type = TransactionType.Income,
                },
                new Category
                {
                    Name = "Inwestycje",
                    Icon = "trending-up",
                    Type = TransactionType.Income,
                },
                new Category
                {
                    Name = "Inne przychody",
                    Icon = "plus-circle",
                    Type = TransactionType.Income,
                },
                new Category
                {
                    Name = "Jedzenie",
                    Icon = "shopping-cart",
                    Type = TransactionType.Expense,
                },
                new Category
                {
                    Name = "Transport",
                    Icon = "truck",
                    Type = TransactionType.Expense,
                },
                new Category
                {
                    Name = "Mieszkanie",
                    Icon = "home",
                    Type = TransactionType.Expense,
                },
                new Category
                {
                    Name = "Rozrywka",
                    Icon = "film",
                    Type = TransactionType.Expense,
                },
                new Category
                {
                    Name = "Zdrowie",
                    Icon = "heart",
                    Type = TransactionType.Expense,
                },
                new Category
                {
                    Name = "Edukacja",
                    Icon = "book",
                    Type = TransactionType.Expense,
                },
                new Category
                {
                    Name = "Ubrania",
                    Icon = "shopping-bag",
                    Type = TransactionType.Expense,
                },
                new Category
                {
                    Name = "Rachunki",
                    Icon = "file-text",
                    Type = TransactionType.Expense,
                },
                new Category
                {
                    Name = "Inne wydatki",
                    Icon = "minus-circle",
                    Type = TransactionType.Expense,
                }
            );

            await _context.SaveChangesAsync(CancellationToken.None);
        }

        // Seed default Asset/Liability categories if missing
        if (
            !_context.Categories.Any(c =>
                c.Type == TransactionType.Asset || c.Type == TransactionType.Liability
            )
        )
        {
            _context.Categories.AddRange(
                new Category
                {
                    Name = "Konto bankowe",
                    Icon = "credit-card",
                    Type = TransactionType.Asset,
                },
                new Category
                {
                    Name = "Inwestycja",
                    Icon = "trending-up",
                    Type = TransactionType.Asset,
                },
                new Category
                {
                    Name = "Kryptowaluty",
                    Icon = "zap",
                    Type = TransactionType.Asset,
                },
                new Category
                {
                    Name = "Nieruchomosc",
                    Icon = "home",
                    Type = TransactionType.Asset,
                },
                new Category
                {
                    Name = "Pozyczka",
                    Icon = "file-text",
                    Type = TransactionType.Liability,
                },
                new Category
                {
                    Name = "Kredyt",
                    Icon = "alert-circle",
                    Type = TransactionType.Liability,
                }
            );

            await _context.SaveChangesAsync(CancellationToken.None);
        }
    }
}
